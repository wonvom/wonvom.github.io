// Kill switch for the old Jekyll blog's service worker (registered at /sw.js).
// Browsers that cached the old site fetch this file, which wipes the cache,
// unregisters itself, and reloads the page so the new site appears.
self.addEventListener("install", () => {
  self.skipWaiting();
});

self.addEventListener("activate", (event) => {
  event.waitUntil(
    (async () => {
      const keys = await caches.keys();
      await Promise.all(keys.map((key) => caches.delete(key)));
      await self.registration.unregister();
      const clients = await self.clients.matchAll({ type: "window" });
      clients.forEach((client) => client.navigate(client.url));
    })(),
  );
});
